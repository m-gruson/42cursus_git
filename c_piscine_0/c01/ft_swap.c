#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	t;
	
	t = *a;
	*a = *b;
	*b = t;
}

int main()
{
	int	a1;
	int	b1;

	a1 = 0;
	b1 = 3;
	
	ft_swap(&a1, &b1);
	printf("%d, %d", a1, b1);
	return 0;
}
